<h2 style="color: #00CCFE; font-size: 62px;">Wie viele Fehler(punkte) darf man bei der Theorieprüfung haben?</h2>
<h2><b style="color: #00CCFE; font-size: 42px;">Jede Frage im Fragenkatalog wird mit einem Wert zwischen 2 und 5 Fehlerpunkten gewichtet. Die Summe aller falsch beantworteten Fragen ergibt so die Gesamtanzahl an Fehlerpunkten. Die Anzahl der erlaubten Fehlerpunkte hängt von der gewählten Führerscheinklasse ab. Bei der Theorieprüfung der Klasse B darf man derzeit maximal 10 Fehlerpunkte haben, allerdings mit einer Einschränkung: Man darf nicht zweimal 5 Fehlerpunkte haben, dann hat man die Theorieprüfung auch nicht bestanden, obwohl man in Summe auch nur 10 Fehlerpunkte hat (siehe Tabelle).</h2>
<h2><b style="color: #00CCFE">Beispiel für die erlaubte Anzahl an Fehlerpunkten für die Klasse B:</b></h2></h2>
